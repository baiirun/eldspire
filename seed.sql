# Archmage Velorin

![[velorin-portrait]]

**Archmage Velorin** (Year 1 - Year 847) was the legendary founder of [[Eldspire]] and the first wielder of the [[Heart of the Spire]].

## Early Life

Born in a small village at the edge of the [[Mistwood Forest]], Velorin showed an aptitude for [[Aetheric Magic]] from a young age. He was discovered by wandering scholars and taken to study with the [[Precursor]] ruins.

> [!tip] Historical Note
> Many historians believe Velorin was not entirely human, possibly having ==Precursor blood== in his lineage.

## Achievements

### The Founding of Eldspire

In Year 1 of the New Calendar, Velorin led a group of settlers to the site of modern-day [[Eldspire]]. Using his mastery of [[Aetheric Magic]], he:

1. Raised the first spire from raw stone
2. Activated the dormant [[Aetheric Crystals]]
3. Established the [[Spellwright Tower]]
4. Created the first ***floating gardens***

### The Great Working

His greatest achievement was the **Great Working**, a spell that bound the [[Heart of the Spire]] to the city itself, ensuring its prosperity for millennia.

```
The incantation of the Great Working:
"By crystal light and ancient stone,
I bind this heart to spire and throne.
Let power flow through every hall,
And Eldspire stand, never to fall."
```

## Legacy

Velorin''s teachings formed the basis of modern [[Aetheric Magic]]. His personal journals are kept in the [[Whispering Library]], though only [[Keeper Mira]] has full access.

