# Eldspire

Eldspire is the towering capital city of the [[Verdant Kingdom]], known for its impossibly tall spires that seem to pierce the clouds themselves.

## History

The city was founded by [[Archmage Velorin]] over two thousand years ago, built upon the ruins of an ancient [[Precursor]] civilization. The original settlers discovered a massive deposit of [[Aetheric Crystals]] beneath the ground, which to this day power the city''s legendary floating gardens and eternal lanterns.

> [!note] Etymology
> The name "Eldspire" comes from the Old Tongue words *eld* (meaning "ancient" or "eternal") and *spire* (referring to the city''s distinctive architecture).

## Districts

The city is divided into several distinct districts:

- **The Pinnacle** - Home to the [[Royal Palace]] and the [[High Council]]
- **The Artisan Quarter** - Where master craftsmen forge ==legendary weapons== and artifacts
- **The Underspire** - The lower levels, shrouded in perpetual twilight
- **The Verdant Ring** - Gardens that encircle the city, maintained by [[Druid Circle of the Green]]

